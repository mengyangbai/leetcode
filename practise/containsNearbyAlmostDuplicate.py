class Solution(object):
    def containsNearbyAlmostDuplicate(self, nums, k, t):
        """
        :type nums: List[int]
        :type k: int
        :type t: int
        :rtype: bool
        """

        i = 0
        while i + k < len(nums):

            i = i + 1

        return False


nums = [1, 0, 1, 1]
k = 1
t = 2
