# Definition for singly-linked list.
class ListNode(object):
    def __init__(self, x):
        self.val = x
        self.next = None


class Solution(object):
    def removeElements(self, head, val):
        """
        :type head: ListNode
        :type val: int
        :rtype: ListNode
        """
        if not head or head.val == val:
            return None

        cur = head
        while cur.next and cur.next.val != val:
            cur = cur.next

        if cur.next:
            cur.next = cur.next.next

        return head


a = Solution()
node1 = ListNode(1)
